function sentenceToJSON(arr) {
    return arr.map(sentence => {
        let result = [];
        for (let i = 1; i < sentence.length - 1; i += 3) {
            result.push({ word: sentence[i], reading: sentence[i + 1], definition: sentence[i + 2] });
        }
        return { category: sentence[0], words: result };
    });
}
